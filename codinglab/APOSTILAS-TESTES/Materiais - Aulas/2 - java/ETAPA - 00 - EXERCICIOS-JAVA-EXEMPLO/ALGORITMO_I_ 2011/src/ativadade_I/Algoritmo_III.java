package ativadade_I;

/**
 3) Faça um programa que imprima a soma, a diferença, o
 * produto e a divisão entre os números inteiros 21 e 42
 */
public class Algoritmo_III {

    public static void main(String[] args) {
        int soma, diferenca, produto, divisao;
        soma = 21 + 42;
        produto = 21 * 42;
        diferenca = 21 - 42;
        divisao = 42 / 21;
        System.out.println("A soma dos numeros é :" + soma);
        System.out.println("O produto dos numeros é :" + produto);
        System.out.println("A diferença dos numeros é :" + diferenca);
        System.out.println("A divisão dos numeros é :" + divisao);
    }
}
